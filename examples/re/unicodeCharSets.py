#!/usr/bin/env python3

"""fixedCharSets.py - unicode characters sets for decimals, alphabetic,
whitespace, identifiers, plus Python functions to "harvest" those sets.
The character sets are represented as tuples of ranges where each range
is a closed intervall represented as a tuple of the first and last
character code."""


from typing import List, Tuple
import unicodedata


#######################################################################
#
# harvesting functions for charsets
#
#######################################################################


def pick(selector) -> List[Tuple[int, str]]:   # e.g. selector = "isdecimal"
    all_digits: List[Tuple[int, str]] = []
    for codepoint in range(0x110000):  # Unicode range ends at 0x10FFFF
        char = chr(codepoint)
        check = getattr(char, selector)
        if check():
            all_digits.append((codepoint, unicodedata.name(char, '')))
    return all_digits


def as_intervals(elements: List[int]) -> List[Tuple[int, int]]:
    """Converts a list of integers into a list of intervals."""
    elements.sort()
    intervals: List[Tuple[int, int]] = []
    start = elements[0]
    last = start
    for element in elements[1:]:
        if element == last + 1:
            last = element
            continue
        intervals.append((start, last))
        start = element
        last = element
    intervals.append((start, last))
    return intervals

def as_python_src(identifier: str, intervals: List[Tuple[int, int]]) -> str:
    """Converts a list of intervals into a Python source code fragment
    using hex-codes for the unicode numbers."""
    src = [f"{identifier} = ["]
    i = 0
    highest_so_far = 0
    for interval in intervals:
        if i > 0:
            src.append(",")
            if (highest_so_far <= 0xff < interval[1]
                    or 0xff < highest_so_far <= 0xffff < interval[1]):
                i += (i % 4 + 1) * 4 - i
            src.append("\n    " if i % 4 == 0 else " ")
        if interval[1] <= 0xff:
            src.append(f"({interval[0]:#04x}, {interval[1]:#04x})")
        elif interval[1] <= 0xffff:
            src.append(f"({interval[0]:#06x}, {interval[1]:#06x})")
        elif interval[1] <= 0xffffff:
            src.append(f"({interval[0]:#08x}, {interval[1]:#08x})")
        else:
            src.append(f" ({interval[0]:#0Ax}, {interval[1]:#0Ax})")
        highest_so_far = interval[1]
        i += 1
    src.append("]\n")
    return ''.join(src)


def quicktest():
    for selector in ("isdecimal", "isspace", "isalpha", "isalnum", "isidentifier"):
        print('\n' + selector + ":")
        all_elements = pick(selector)
        print(len(all_elements), all_elements[:100])
        intervals = as_intervals([e[0] for e in all_elements])
        print(len(intervals), intervals[:100])


def generate_src() -> str:
    fragments = []
    chsets = dict()
    for selector in ("isdecimal", "isspace", "isalpha", "isalnum"):
        all_elements = pick(selector)
        intervals = as_intervals([e[0] for e in all_elements])
        chsets[selector] = intervals
        fragments.append(as_python_src(selector[2:], intervals))
    identifier = sorted(set(chsets['isalnum']) | {(0x5f, 0x5f)})
    fragments.append(as_python_src('identifier', identifier))
    return "\n\n".join(fragments)


